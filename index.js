const addWallCell = (boardCell) => {
  boardCell.classList.add("cell", "wall");
  return boardCell;
};

const addPacDot = (boardCell) => {
  boardCell.classList.add("cell", "pac-dot");
  return boardCell;
};

const addGhostLair = (boardCell) => {
  boardCell.classList.add("cell", "ghost-lair");
  return boardCell;
};

const addPowerPellet = (boardCell) => {
  boardCell.classList.add("cell", "power-pellet");
  return boardCell;
};

const addEmptyCell = (boardCell) => {
  boardCell.classList.add("cell", "empty-cell");
  return boardCell;
};

const createGameBoard = (layout) => {
  layout.forEach((cell) => {
    const boardCell = document.createElement("div");

    switch (cell) {
      case 0:
        grid.appendChild(addPacDot(boardCell));
        break;
      case 1:
        grid.appendChild(addWallCell(boardCell));
        break;
      case 2:
        grid.appendChild(addGhostLair(boardCell));
        break;
      case 3:
        grid.appendChild(addPowerPellet(boardCell));
        break;
      case 4:
        grid.appendChild(addEmptyCell(boardCell));
        break;
    }
  });
};
createGameBoard(layout);
